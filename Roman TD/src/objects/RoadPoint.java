package objects;

public class RoadPoint {
	private int xCord, yCord;
	
	public RoadPoint (int xCord, int yCord) {
		this.xCord = xCord;
		this.yCord = yCord;
		
	}

	public int getxCord() {
		return xCord;
	}

	public void setxCord(int xCord) {
		this.xCord = xCord;
	}

	public int getyCord() {
		return yCord;
	}

	public void setyCord(int yCord) {
		this.yCord = yCord;
	}

	
	
	
}
